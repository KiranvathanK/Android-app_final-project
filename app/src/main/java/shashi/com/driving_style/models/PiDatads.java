package shashi.com.driving_style.models;

public class PiDatads {
    public String name;
    public String value;

    public PiDatads(String name, String value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public String getValue() {
        return value;
    }
}
